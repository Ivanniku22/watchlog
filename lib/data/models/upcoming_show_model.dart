class UpcomingShow {
  final int id;
  final String title;
  final String poster;
  final String season;
  final String episode;
  final String episodeTitle;
  final String time;
  final String studio;

  UpcomingShow({
    required this.title,
    required this.poster,
    required this.season,
    required this.episode,
    required this.episodeTitle,
    required this.id,
    required this.studio,
    required this.time,
  });
}